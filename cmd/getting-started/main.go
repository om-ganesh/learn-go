package main

// Step1. Declare a main package (a package is a way to group functions, and it's made up of all the files in the same directory).
// Step2. Import the popular fmt package, which contains functions for formatting text, including printing to the console.
import (
	"fmt"
)

// Step3. A main function executes by default when you run the main package.
func main() {
	fmt.Println("Welcome to Learning Go!!!")
}
